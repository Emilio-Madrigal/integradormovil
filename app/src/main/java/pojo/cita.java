package pojo;

public class cita {

    public int dia;
    public int mes;
    public int Year;
    public int horas;
    public int minutos;

    //setters y getters de calendario
    public void setDia(int dia) {
        this.dia = dia;
    }

    public void setMes(int mes) {
        this.mes = mes;
    }

    public void setYear(int Year) {
        this.Year = Year;
    }

    public void setHoras(int horas) {
        this.horas = horas;
    }

    public void setMinutos(int minutos) {
        this.minutos = minutos;
    }

    public int getDia() {
        return dia;
    }

    public int getMes() {
        return mes;
    }

    public int getAno() {
        return Year;
    }

    public int getHoras() {
        return horas;
    }

    public int getMinutos() {
        return minutos;
    }



}
